export function getCityBySlug(slug: string): CityPage | undefined {
  return cities.find(c => c.slug === slug)
}

export function getSchoolsInCity(citySlug: string): { name: string; slug: string; stateSlug: string }[] {
  const city = getCityBySlug(citySlug)
  if (!city) return []
  const stateData = states.find(s => s.slug === city.stateSlug)
  if (!stateData) return []
  return stateData.schools
    .filter(school => city.topSchools.some(ts => school.name.toLowerCase().includes(ts.toLowerCase().split(' ')[0])))
    .map(school => ({ name: school.name, slug: school.slug, stateSlug: city.stateSlug }))
    .slice(0, 8)
}
